class Comparision 
{
    public static void main(String[] args)
    {
        boolean Equal = (5 == 5);
        System.out.println("Equal to: " + Equal);
        
        boolean Notequal = (10 != 5);
        System.out.println("Equal to: " + Notequal);
        
        boolean Great = (8 > 3);
        System.out.println("Equal to: " + Great);
        
        boolean Lesser = (4 < 9);
        System.out.println("Equal to: " + Lesser);
        
        boolean GreatEqual = (7 >= 6);
        System.out.println("Equal to: " + GreatEqual);
        
        boolean LesserEqual = (2 <= 3);
        System.out.println("Equal to: " + LesserEqual);
    }    
}
